/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USB_DEVICE_Init();
  MX_ADC1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  /* USER CODE BEGIN 2 */

  // --- TCD1304 STARTUP SEQUENCE (Fixed for Slave Mode Synchronization) ---

  // 1. Start Master Clock (2MHz) on PA6
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);

  // 2. Start ADC Trigger (500kHz) - Internal
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);

  // 3. ARM THE SLAVE: Start SH (Shift Gate) on PA2
  // It is now waiting for the trigger on the ITR0 line
  HAL_TIM_PWM_Start(&htim5, TIM_CHANNEL_3);

  // 4. START THE MASTER: Start ICG (Reset Gate) on PA0
  // This sends the trigger to TIM5 immediately
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);

  // 5. Set Delay (72 ticks @ 120MHz = ~600ns)
  __HAL_TIM_SET_COUNTER(&htim2, 72);

  // 6. Start ADC in DMA Circular Mode
  HAL_ADC_Start_DMA(&hadc1, (uint32_t *)CCDPixelBuffer, CCD_BUFFER_SIZE);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1) {

    // --- USB COMMAND HANDLING ---
    if (usb_cmd_ready) {
      usb_cmd_ready = 0;

      if (!session_started) {
        session_started = 1;
        const char *banner = "\r\n=== TCD1304 CCD Controller (H7) ===\r\n"
                             "Commands:\r\n"
                             "  shot   - Capture one frame\r\n"
                             "  stream - Start continuous data\r\n"
                             "  stop   - Stop streaming\r\n"
                             "  status - Show debug info\r\n"
                             "> ";
        CDC_Send_Wait(banner, strlen(banner));
      }

      // Process Command
      if (strlen(usb_cmd) > 0) {
        if (strcmp(usb_cmd, "shot") == 0) {
          single_shot = 1;
          CDC_Transmit_FS((uint8_t *)"Capturing...\r\n", 14);
        } else if (strcmp(usb_cmd, "stream") == 0) {
          is_streaming = 1;
          CDC_Transmit_FS((uint8_t *)"Streaming Started (Ctrl+C to stop)\r\n",
                          36);
        } else if (strcmp(usb_cmd, "stop") == 0) {
          is_streaming = 0;
          single_shot = 0;
          CDC_Transmit_FS((uint8_t *)"Streaming Stopped\r\n", 19);
        } else if (strcmp(usb_cmd, "status") == 0) {
          char msg[64];
          // Read a few pixels to check wiring
          uint16_t pix100 = CCDPixelBuffer[100];
          uint16_t pix2000 = CCDPixelBuffer[2000];
          sprintf(msg, "Status: Running\r\nP[100]=%d, P[2000]=%d\r\n", pix100,
                  pix2000);
          CDC_Send_Wait(msg, strlen(msg));
        } else if (strcmp(usb_cmd, "check") == 0) {
          char msg[128];
          sprintf(msg, "TIM3_ARR: %lu\r\nTIM2_ARR: %lu\r\nTIM5_ARR: %lu\r\n",
                  TIM3->ARR, TIM2->ARR, TIM5->ARR);
          CDC_Send_Wait(msg, strlen(msg));
        } else {
          CDC_Transmit_FS((uint8_t *)"Unknown cmd\r\n", 13);
        }
      }

      // Print Prompt
      memset(usb_cmd, 0, sizeof(usb_cmd));
      if (!is_streaming)
        CDC_Transmit_FS((uint8_t *)"> ", 2);
    }

    // --- DATA TRANSMISSION ---
    if (buffer_updated) {
      // This flag is set by the DMA interrupt ~133 times per second
      buffer_updated = 0;

      if (single_shot) {
        Send_CCD_Frame();
        single_shot = 0;                     // Clear flag
        CDC_Transmit_FS((uint8_t *)"> ", 2); // Print prompt
      } else if (is_streaming) {
        Send_CCD_Frame();
        // In streaming mode, we don't print the prompt
      }
    }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

