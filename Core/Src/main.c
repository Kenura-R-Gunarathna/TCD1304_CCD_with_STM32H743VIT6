// Helper: Send text and wait (prevents USB buffer overflow)
void CDC_Send_Wait(const char *ptr, uint16_t len) {
  uint32_t start = HAL_GetTick();
  extern USBD_HandleTypeDef hUsbDeviceFS;
  USBD_CDC_HandleTypeDef *hcdc =
      (USBD_CDC_HandleTypeDef *)hUsbDeviceFS.pClassData;

  while (hcdc->TxState != 0 && (HAL_GetTick() - start < 10))
    ; // Wait max 10ms
  CDC_Transmit_FS((uint8_t *)ptr, len);
}

// Helper: Send one frame of CCD data as CSV text
void Send_CCD_Frame(void) {
  char msg[32];

  // Header
  const char *head = "START_FRAME\r\n";
  CDC_Send_Wait(head, strlen(head));

  // Send all pixels
  for (int i = 0; i < CCD_BUFFER_SIZE; i++) {
    // Format: Index, Value
    // Note: We skip the first 32 dummies usually, but let's send everything for
    // debug
    sprintf(msg, "%d,%d\r\n", i, CCDPixelBuffer[i]);
    CDC_Send_Wait(msg, strlen(msg));
  }

  // Footer
  const char *foot = "END_FRAME\r\n";
  CDC_Send_Wait(foot, strlen(foot));
}

// Callback: Called when DMA finishes filling the buffer
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {
  // STM32H7 Cache Maintenance:
  // The DMA writes to RAM, but the CPU reads from Cache.
  // We must invalidate the cache to see the new data.
  SCB_CleanInvalidateDCache_by_Addr((uint32_t *)CCDPixelBuffer,
                                    CCD_BUFFER_SIZE * 2);

  buffer_updated = 1;
}
